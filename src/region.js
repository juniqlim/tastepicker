/**
 * 픽커가 적은 지역은 제각각이다. `연남`, `연남동`, `종암동`, `롯데월드몰` 이 다 따로 센다.
 * 그래서 픽커의 말 대신 장소의 주소에서 지역을 잡는다. 주소는 형식이 하나다.
 *
 * 시·군·구까지만 본다. 동까지 내려가면 다시 잘게 쪼개져 목록이 못 쓰게 된다.
 */

/** 같은 곳을 달리 적은 것을 하나로 모은다. 이름이 바뀐 곳은 옛 이름도 함께 들어온다. */
const SHORT = {
  서울특별시: '서울', 부산광역시: '부산', 대구광역시: '대구', 인천광역시: '인천',
  광주광역시: '광주', 대전광역시: '대전', 울산광역시: '울산',
  경기도: '경기', 강원도: '강원', 강원특별자치도: '강원',
  충청북도: '충북', 충청남도: '충남',
  전라북도: '전북', 전북특별자치도: '전북', 전라남도: '전남',
  경상북도: '경북', 경상남도: '경남',
  제주도: '제주', 제주특별자치도: '제주',
}

/** 구가 없는 곳. 시 이름 하나로 끝난다. */
const ALONE = { 세종특별자치시: '세종', 세종시: '세종' }

export function regionOf(address) {
  if (!address) return null

  const [head, next] = address.trim().split(/\s+/)
  if (ALONE[head]) return ALONE[head]

  // 우리말 시도 이름으로 시작하지 않으면 국내 주소가 아니다.
  const sido = SHORT[head]
  if (!sido) return '해외'

  return next ? `${sido} ${next}` : sido
}
